---Возвращает ширину окна программы.
---@return number
---@nodiscard
function ScrW()

	return rl.GetScreenWidth()

end

---Возвращает высоту окна программы.
---@return number
---@nodiscard
function ScrH()

	return rl.GetScreenHeight()

end

